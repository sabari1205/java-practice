public class DeclareDouble {
    public static void main(String[] args) {
        double pi = 3.14159;
        double radius = 7.5;
        double area = pi * radius * radius;
        System.out.println("Pi = " + pi);
        System.out.println("Radius = " + radius);
        System.out.println("Area = " + area);
    }
}
